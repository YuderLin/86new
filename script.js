//kurena
$('.Kurena').click(function(event) {
	$('.Kurena').css('background-image', 'url(img/Kurena-active.png)');
});
$('.Kurena').mouseover(function(event) {
	  $('.Kurena').css('background-image', 'url(img/Kurena-hover.png)');
  });

$('.Kurena').mouseleave(function(ebent){
	$('.Kurena').css('background-image','url(img/Kurena_1.png)')
});

